    // ── Basic markdown patterns ──

    #[test]
    fn empty_input_yields_empty_line() {
        let lines = markdown_to_lines("");
        assert_eq!(lines.len(), 1);
        assert!(matches!(lines[0], RenderLine::Empty));
    }

    #[test]
    fn plain_paragraph() {
        let lines = markdown_to_lines("Hello world");
        assert_eq!(lines.len(), 1);
        assert!(matches!(&lines[0], RenderLine::Text { role: LineRole::AgentMessage, spans, .. } if spans.len() == 1));
    }

    #[test]
    fn two_paragraphs() {
        let lines = markdown_to_lines("First para.\n\nSecond para.");
        assert_eq!(lines.len(), 2);
        assert!(matches!(&lines[0], RenderLine::Text { .. }));
        assert!(matches!(&lines[1], RenderLine::Text { .. }));
    }

    #[test]
    fn headings_all_levels() {
        let md = "# H1\n## H2\n### H3\n#### H4\n##### H5\n###### H6";
        let lines = markdown_to_lines(md);
        assert_eq!(lines.len(), 6);
        for (i, line) in lines.iter().enumerate() {
            let expected_level = (i + 1) as u8;
            assert!(
                matches!(line, RenderLine::Text { role: LineRole::Heading(l), .. } if *l == expected_level),
                "line {i} should be Heading({expected_level})"
            );
        }
    }

    #[test]
    fn unordered_list() {
        let md = "- alpha\n- beta\n- gamma";
        let lines = markdown_to_lines(md);
        assert_eq!(lines.len(), 3);
        for line in &lines {
            assert!(matches!(line, RenderLine::Text { role: LineRole::UnorderedListItem(0), .. }));
        }
    }

    #[test]
    fn ordered_list() {
        let md = "1. first\n2. second\n3. third";
        let lines = markdown_to_lines(md);
        assert_eq!(lines.len(), 3);
        for (i, line) in lines.iter().enumerate() {
            let expected_num = (i + 1) as u32;
            assert!(
                matches!(line, RenderLine::Text { role: LineRole::OrderedListItem { num, indent: 0 }, .. } if *num == expected_num)
            );
        }
    }

