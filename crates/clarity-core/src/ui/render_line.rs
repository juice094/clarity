//! Semantic line model for chat content — ADR-012 implementation.
//!
//! Replaces pixel-continuous markdown streams with discrete typed lines.
//! Each line carries enough structural information for both GUI (egui)
//! and TUI (ratatui) renderers to produce correct output without re-parsing.
//!
//! ## Design principles
//!
//! - **Line-atomic**: The smallest unit of rendering is one line. Virtual scrolling,
//!   j/k navigation, and streaming append all operate on `Vec<RenderLine>`.
//! - **Frontend-neutral**: No egui or ratatui types leak into this module.
//! - **Graceful degradation**: Complex blocks (tables, images, full-screen Plan)
//!   fall back to `BlockSlot` and are rendered by the existing block pipeline.

use serde::{Deserialize, Serialize};
use smol_str::SmolStr;

// ============================================================================
// Line model
// ============================================================================

/// A single semantic line in the chat stream.
///
/// 13 variants absorb 30+ markdown / UI patterns per ADR-012.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case", tag = "kind", content = "value")]
pub enum RenderLine {
    /// Plain text with optional inline spans and semantic role.
    Text {
        spans: Vec<Span>,
        role: LineRole,
        indent: u8,
    },

    /// One line inside a fenced code block.
    CodeLine {
        lang: SmolStr,
        content: SmolStr,
        line_no: Option<u32>,
        diff: DiffKind,
    },

    /// Header line for a tool call block (expandable).
    ToolCallHeader {
        name: SmolStr,
        status: ToolStatus,
        expanded: bool,
    },

    /// A single argument row inside an expanded tool call.
    ToolCallArg {
        key: SmolStr,
        value: SmolStr,
    },

    /// Collapsible thinking / reasoning block (ClaudeCode-borrowed).
    Thinking {
        content: SmolStr,
        collapsed: bool,
    },

    /// Interactive approval prompt with options.
    ApprovalPrompt {
        options: Vec<ApprovalOption>,
    },

    /// Transient or persistent status indicator (spinner, progress, network).
    StatusLine {
        kind: StatusKind,
        content: SmolStr,
        transient: bool,
    },

    /// Reference to an Artifact (ClaudeCode-borrowed, rendered as a card).
    ArtifactRef {
        artifact_id: ArtifactId,
        summary: SmolStr,
    },

    /// Cross-instance mention — clicking navigates to the target instance/session.
    CrossInstanceRef {
        target_instance: InstanceId,
        target_session: Option<SessionId>,
        message: SmolStr,
    },

    /// Slash-command autocomplete suggestion row.
    SlashCompletion {
        command: SmolStr,
        description: SmolStr,
    },

    /// Blinking cursor at the end of a streaming response.
    StreamingCursor,

    /// Horizontal rule (--- or *** in markdown).
    Divider,

    /// Empty line — preserves vertical spacing from the original markdown.
    Empty,

    /// Escape hatch for blocks that cannot be line-atomised (tables, images, Plan).
    /// The frontend delegates to the existing `RenderBlock` pipeline.
    BlockSlot {
        block_id: BlockId,
        line_count: u8,
    },
}

/// Inline text span with a semantic style tag.
///
/// Frontends map `SpanStyle` to concrete colors/fonts via their theme system.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct Span {
    pub text: SmolStr,
    pub style: SpanStyle,
}

impl Span {
    pub fn plain(text: impl Into<SmolStr>) -> Self {
        Self {
            text: text.into(),
            style: SpanStyle::Normal,
        }
    }

    pub fn styled(text: impl Into<SmolStr>, style: SpanStyle) -> Self {
        Self {
            text: text.into(),
            style,
        }
    }
}

/// Semantic style tags for inline spans.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum SpanStyle {
    Normal,
    Bold,
    Italic,
    BoldItalic,
    Code,
    Link,
    Mention,   // @user / @instance
    FileRef,   // @path/to/file
    Highlight, // search match / selection
}

/// The "voice" or structural role of a line.
///
/// Determines left-margin treatment, icon, and color baseline in the renderer.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case", tag = "kind", content = "value")]
pub enum LineRole {
    /// Message sent by the user.
    UserMessage,
    /// Message generated by the agent.
    AgentMessage,
    /// System notice (errors, warnings from the runtime).
    SystemMessage,
    /// Explicit error output.
    ErrorMessage,
    /// Markdown heading level 1–6.
    Heading(u8),
    /// Blockquote line.
    Quote,
    /// Bullet list item at a given indent depth (0 = top level).
    UnorderedListItem(u8),
    /// Numbered list item.
    OrderedListItem { num: u32, indent: u8 },
    /// @-mention of a user or instance.
    Mention,
    /// File path reference.
    FileRef,
    /// Informative status line.
    Status,
    /// Warning line.
    Warning,
    /// Contextual note.
    Note,
    /// Token usage summary (e.g. "47K / 200K").
    TokenUsage,
    /// Context compaction notice.
    ContextCompaction,
    /// openclaw-compatible sandbox indicator.
    Sandbox,
}

/// Diff kind for code lines (Git-style hunk rendering).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum DiffKind {
    #[default]
    Normal,
    Added,
    Removed,
    Context,
}

/// Tool call lifecycle status.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ToolStatus {
    Running,
    Success,
    Warning,
    Error,
}

/// Status indicator kind for `StatusLine`.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case", tag = "kind", content = "value")]
pub enum StatusKind {
    Spinner,
    Progress { current: u32, total: u32 },
    Network,
    Compaction,
    ModelSwitch,
}

/// Interactive approval option (1/2/3 key selection).
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case", tag = "kind", content = "value")]
pub enum ApprovalOption {
    Yes,
    YesAndRemember,
    No { reason_required: bool },
    Custom(SmolStr),
}

