/// Convert a markdown string into a sequence of `RenderLine`s.
///
/// This is a **lossy** conversion in one direction: tables and images degrade
/// to `BlockSlot`, and complex nested HTML is stripped. The goal is to produce
/// a line stream that covers 90 % of real agent output (headings, lists, code,
/// paragraphs, blockquotes) while delegating the remainder to the block pipeline.
///
/// ## Streaming readiness
///
/// The converter operates on complete markdown chunks. For streaming, the
/// caller buffers until a natural boundary (newline, closing fence, end of
/// block element) and re-parses the accumulated chunk. `StreamingCursor` is
/// appended manually by the frontend between flushes.
pub fn markdown_to_lines(md: &str) -> Vec<RenderLine> {
    use pulldown_cmark::{Event, Parser, Tag, TagEnd};

    let mut lines: Vec<RenderLine> = Vec::new();
    let mut current_text = String::new();
    let mut in_code_block = false;
    let mut code_lang = SmolStr::default();
    let mut code_lines: Vec<String> = Vec::new();
    let mut list_stack: Vec<u8> = Vec::new(); // indent depths
    let mut ordered_num: u32 = 0;
    let mut heading_level: u8;
    let mut role_stack: Vec<LineRole> = Vec::new();
    let mut current_role = LineRole::AgentMessage;
    let mut in_table = false;
    let mut table_rows = 0usize;
    let mut in_image = false;

    let flush_with_role = |buf: &mut String, out: &mut Vec<RenderLine>, role: LineRole, indent: u8| {
        if !buf.trim().is_empty() {
            out.push(RenderLine::Text {
                spans: vec![Span::plain(std::mem::take(buf))],
                role,
                indent,
            });
        } else if !buf.is_empty() {
            out.push(RenderLine::Empty);
        }
        buf.clear();
    };

    let parser = Parser::new(md);
    for event in parser {
        match event {
            Event::Start(Tag::CodeBlock(lang)) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                in_code_block = true;
                code_lang = match lang {
                    pulldown_cmark::CodeBlockKind::Fenced(l) => l.to_string().into(),
                    pulldown_cmark::CodeBlockKind::Indented => SmolStr::default(),
                };
                code_lines.clear();
            }
            Event::End(TagEnd::CodeBlock) => {
                in_code_block = false;
                // Filter trailing empty lines produced by terminal newlines.
                while let Some(last) = code_lines.last() {
                    if last.trim().is_empty() {
                        code_lines.pop();
                    } else {
                        break;
                    }
                }
                for (i, content) in code_lines.iter().enumerate() {
                    lines.push(RenderLine::CodeLine {
                        lang: code_lang.clone(),
                        content: content.clone().into(),
                        line_no: Some((i + 1) as u32),
                        diff: DiffKind::Normal,
                    });
                }
                code_lines.clear();
                code_lang = SmolStr::default();
            }
            Event::Start(Tag::List(start_num)) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                list_stack.push(list_stack.last().copied().unwrap_or(0) + 1);
                ordered_num = start_num.map(|n| n as u32).unwrap_or(0);
            }
            Event::End(TagEnd::List(_)) => {
                list_stack.pop();
            }
            Event::Start(Tag::Item) => {
                role_stack.push(current_role);
                let depth = list_stack.last().copied().unwrap_or(0).saturating_sub(1);
                current_role = if ordered_num > 0 {
                    let num = ordered_num;
                    ordered_num += 1;
                    LineRole::OrderedListItem { num, indent: depth }
                } else {
                    LineRole::UnorderedListItem(depth)
                };
            }
            Event::End(TagEnd::Item) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                current_role = role_stack.pop().unwrap_or(LineRole::AgentMessage);
            }
            Event::Start(Tag::Heading { level, .. }) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                role_stack.push(current_role);
                heading_level = level as u8;
                current_role = LineRole::Heading(heading_level);
            }
            Event::End(TagEnd::Heading(_)) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                current_role = role_stack.pop().unwrap_or(LineRole::AgentMessage);
            }
            Event::Start(Tag::BlockQuote(_)) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                role_stack.push(current_role);
                current_role = LineRole::Quote;
            }
            Event::End(TagEnd::BlockQuote(_)) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                current_role = role_stack.pop().unwrap_or(LineRole::AgentMessage);
            }
            Event::Start(Tag::Table(_)) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                in_table = true;
                table_rows = 0;
            }
            Event::End(TagEnd::TableRow) if in_table => {
                table_rows += 1;
            }
            Event::End(TagEnd::Table) => {
                in_table = false;
                lines.push(RenderLine::BlockSlot {
                    block_id: "table".into(),
                    line_count: table_rows.max(1) as u8,
                });
            }
            Event::Start(Tag::Image { .. }) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                in_image = true;
            }
            Event::End(TagEnd::Image) => {
                in_image = false;
                lines.push(RenderLine::BlockSlot {
                    block_id: "image".into(),
                    line_count: 3,
                });
            }
            Event::Start(Tag::Paragraph) => {
                // Paragraphs are the default; no role change needed.
            }
            Event::End(TagEnd::Paragraph) => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
            }
            Event::Text(t) | Event::Code(t) => {
                if in_code_block {
                    for line in t.split('\n') {
                        code_lines.push(line.to_string());
                    }
                } else if in_table || in_image {
                    // Swallow content inside unsupported block-level slots.
                } else {
                    current_text.push_str(&t);
                }
            }
            Event::SoftBreak | Event::HardBreak if !in_code_block && !in_table && !in_image => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
            }
            Event::Rule => {
                flush_with_role(&mut current_text, &mut lines, current_role, 0);
                lines.push(RenderLine::Divider);
            }
            Event::Html(html) => {
                current_text.push_str(&html);
            }
            _ => {
                // Inline formatting (Strong, Emphasis, Link, etc.) is flattened
                // to plain text in the skeleton; S5 (renderer) introduces Span
                // style extraction from the event stream.
            }
        }
    }

    flush_with_role(&mut current_text, &mut lines, current_role, 0);

    if lines.is_empty() {
        lines.push(RenderLine::Empty);
    }

    lines
}

